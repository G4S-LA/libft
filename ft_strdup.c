#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t		i;
	char		*str;

	i = -1;
	str = (char*)malloc(sizeof(char) * (ft_strlen(src) + 1));
	if (!str)
		return (NULL);
	while (src[++i])
		str[i] = src[i];
	str[i] = '\0';
	return (str);
}
